(function() {
    'use strict';

    let activeBackdrop = null;
    let activeResolve = null;
    let lastFocused = null;
    let prevBodyOverflow = '';
    let prevBodyPaddingRight = '';

    function escapeHtml(s) {
        return String(s == null ? '' : s).replace(/[&<>"']/g, c => (
            { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]
        ));
    }

    function close(result) {
        if (!activeBackdrop) return;
        const backdrop = activeBackdrop;
        const resolve = activeResolve;
        activeBackdrop = null;
        activeResolve = null;

        document.removeEventListener('keydown', handleKey, true);

        // –ź–Ĺ–ł–ľ–į—Ü–ł—Ź –∑–į–ļ—Ä—č—ā–ł—Ź ‚ÄĒ backdrop fade-out, modal slide-down
        backdrop.classList.remove('is-open');
        // –£–ī–į–Ľ—Ź–Ķ–ľ –Ņ–ĺ—Ā–Ľ–Ķ –į–Ĺ–ł–ľ–į—Ü–ł–ł (--dur-base ‚Čą 200ms)
        setTimeout(() => {
            backdrop.remove();
            // –í–ĺ—Ā—Ā—ā–į–Ĺ–į–≤–Ľ–ł–≤–į–Ķ–ľ overflow –ł padding-right body –≤ –ł—Ā—Ö–ĺ–ī–Ĺ—č–Ķ –∑–Ĺ–į—á–Ķ–Ĺ–ł—Ź
            document.body.style.overflow = prevBodyOverflow;
            document.body.style.paddingRight = prevBodyPaddingRight;
            if (lastFocused && typeof lastFocused.focus === 'function') {
                try { lastFocused.focus(); } catch (e) {}
            }
            lastFocused = null;
        }, 220);

        if (resolve) resolve(result);
    }

    function handleKey(e) {
        if (!activeBackdrop) return;
        if (e.key === 'Escape') {
            e.preventDefault();
            e.stopPropagation();
            close(false);
            return;
        }
        if (e.key === 'Enter') {
            const cancel = activeBackdrop.querySelector('[data-mv-confirm-cancel]');
            if (document.activeElement !== cancel) {
                e.preventDefault();
                e.stopPropagation();
                close(true);
            }
            return;
        }
        if (e.key === 'Tab') {
            const focusables = activeBackdrop.querySelectorAll('button');
            if (!focusables.length) return;
            const first = focusables[0];
            const last  = focusables[focusables.length - 1];
            if (e.shiftKey && document.activeElement === first) {
                e.preventDefault();
                last.focus();
            } else if (!e.shiftKey && document.activeElement === last) {
                e.preventDefault();
                first.focus();
            }
        }
    }

    function open(opts) {
        if (activeBackdrop) close(false);

        const o = opts || {};
        const title       = o.title       || '–ü–ĺ–ī—ā–≤–Ķ—Ä–ī–ł—ā–Ķ –ī–Ķ–Ļ—Ā—ā–≤–ł–Ķ';
        const message     = o.message     || '';
        const confirmText = o.confirmText || 'OK';
        const cancelText  = o.cancelText  || '–ě—ā–ľ–Ķ–Ĺ–į';
        const danger      = !!o.danger;

        lastFocused = document.activeElement;

        const backdrop = document.createElement('div');
        backdrop.className = 'modal-backdrop';
        backdrop.setAttribute('role', 'dialog');
        backdrop.setAttribute('aria-modal', 'true');
        backdrop.setAttribute('aria-labelledby', 'mv-confirm-title');
        backdrop.innerHTML = `
            <div class="modal" role="document">
                <div class="modal-header">
                    <h3 id="mv-confirm-title" class="modal-title">${escapeHtml(title)}</h3>
                </div>
                ${message ? `<div class="modal-body">${escapeHtml(message)}</div>` : ''}
                <div class="modal-footer">
                    <button type="button" class="btn btn--ghost" data-mv-confirm-cancel>${escapeHtml(cancelText)}</button>
                    <button type="button" class="btn ${danger ? 'btn--danger' : 'btn--primary'}" data-mv-confirm-ok>${escapeHtml(confirmText)}</button>
                </div>
            </div>
        `;

        // Click handlers
        backdrop.addEventListener('click', (e) => {
            // Click –Ņ–ĺ backdrop (–Ĺ–Ķ –Ņ–ĺ modal) ‚ÄĒ –ĺ—ā–ľ–Ķ–Ĺ–į
            if (e.target === backdrop) {
                close(false);
                return;
            }
            if (e.target.closest('[data-mv-confirm-cancel]')) {
                close(false);
                return;
            }
            if (e.target.closest('[data-mv-confirm-ok]')) {
                close(true);
                return;
            }
        });

        document.body.appendChild(backdrop);

        // Body scroll lock + –ļ–ĺ–ľ–Ņ–Ķ–Ĺ—Ā–į—Ü–ł—Ź scrollbar gutter shift.
        // –ü—Ä–ĺ–Ī–Ľ–Ķ–ľ–į: –ļ–ĺ–≥–ī–į body.overflow=hidden ‚ÄĒ –≤–Ķ—Ä—ā–ł–ļ–į–Ľ—Ć–Ĺ—č–Ļ scrollbar –≤—č–ļ–Ľ—é—á–į–Ķ—ā—Ā—Ź
        // –ł –≤–Ķ—Ā—Ć –ļ–ĺ–Ĺ—ā–Ķ–Ĺ—ā –ī—Ď—Ä–≥–į–Ķ—ā—Ā—Ź –≤–Ņ—Ä–į–≤–ĺ –Ĺ–į —ą–ł—Ä–ł–Ĺ—É scrollbar (~15px). –Ē–Ľ—Ź –ļ–ĺ–ľ–Ņ–Ķ–Ĺ—Ā–į—Ü–ł–ł
        // –ī–ĺ–Ī–į–≤–Ľ—Ź–Ķ–ľ —Ä–į–≤–Ĺ—č–Ļ padding-right –Ĺ–į body ‚ÄĒ –ļ–ĺ–Ĺ—ā–Ķ–Ĺ—ā –ĺ—Ā—ā–į—Ď—ā—Ā—Ź –Ĺ–į –ľ–Ķ—Ā—ā–Ķ.
        const scrollbarWidth = window.innerWidth - document.documentElement.clientWidth;
        prevBodyOverflow = document.body.style.overflow;
        prevBodyPaddingRight = document.body.style.paddingRight;
        document.body.style.overflow = 'hidden';
        if (scrollbarWidth > 0) {
            document.body.style.paddingRight = scrollbarWidth + 'px';
        }

        activeBackdrop = backdrop;
        document.addEventListener('keydown', handleKey, true);

        // –ě—ā–ļ—Ä—č–≤–į–Ķ–ľ (next frame –ī–Ľ—Ź CSS transition).
        // .is-open –ī–ĺ–Ī–į–≤–Ľ—Ź–Ķ—ā—Ā—Ź –ė–ó JS ‚ÄĒ backdrop —Ā—ā–į–Ĺ–ĺ–≤–ł—ā—Ā—Ź display:flex –ł –∑–į–Ņ—É—Ā–ļ–į–Ķ—ā—Ā—Ź animation.
        requestAnimationFrame(() => {
            backdrop.classList.add('is-open');
            const okBtn = backdrop.querySelector('[data-mv-confirm-ok]');
            if (okBtn) okBtn.focus();
        });

        return new Promise((resolve) => {
            activeResolve = resolve;
        });
    }

    // Public API
    window.MineVPN = window.MineVPN || {};
    window.MineVPN.confirm = open;
})();
