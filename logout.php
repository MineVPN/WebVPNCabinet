<?php
// ==============================================================================
// MINE SERVER - Выход из системы
// ==============================================================================

session_start();

// Полное уничтожение сессии
$_SESSION = array();

// Удаление cookie сессии
if (isset($_COOKIE[session_name()])) {
    setcookie(session_name(), '', time() - 3600, '/');
}

// Уничтожение сессии
session_destroy();

// Перенаправление на страницу входа
header("Location: login.php");
exit();
